function doCompoundOperation(){
    let x:number=4;
    console.log("Inital value of x is "+x);
    x+=2;
    console.log("After compound statement x+=2 value of x is "+x);
    x-=1;
    console.log("After compound statement x-=1 value of x is "+x);
    x*=2;
    console.log("After compound statement x*=2 value of x is "+x);
    x/=2;
    console.log("After compound statement x/=2 value of x is "+x);


}

doCompoundOperation();